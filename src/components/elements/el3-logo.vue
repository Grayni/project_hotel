<script>
export default {
  name: 'logo',
  props: ['msg', 'position'],
  data () {
    return {
      logoElements: document.body.getElementsByClassName('branch'),
      linkHome: location.protocol + '//' + location.host,
      logoView: setTimeout(this.logoPresent, 200),
      widthLogo: '120px',
      heightLogo: '120px',
      cornerZiro: false
    }
  },
  methods: {
    logoPresent () {
      [...this.logoElements].map(item => item.classList.add('active'))
    },
    logoHidden () {
      [...this.logoElements].map(item => item.classList.remove('active'))
    },
    posLogo () {
      if (this.position > 0) {
        this.cornerZiro = true
        this.widthLogo = '60px'
        this.heightLogo = '60px'
        return this.cornerZiro
      } else {
        this.cornerZiro = false
        this.widthLogo = '120px'
        this.heightLogo = '120px'
        return this.cornerZiro
      }
    }
  },
  updated () {
    this.logoHidden()
    setTimeout(this.logoPresent, 200)
  },
  mounted () {
    this.$nextTick(() => this.posLogo())
    return this.logoView
  },
  watch: {
    position () {
      this.posLogo()
    }
  }
}
</script>

